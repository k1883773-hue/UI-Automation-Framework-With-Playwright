export class Signup {
    name;
    email;
    password;
    day;
    month;
    year;
    firstName;
    lastName;
    address;
    country;
    state;
    city;
    zipcode;
    mobileNumber;

    getName() { return this.name; }
    setName(value) { this.name = value; }

    getEmail() { return this.email; }
    setEmail(value) { this.email = value; }

    getPassword() { return this.password; }
    setPassword(value) { this.password = value; }

    getDay() { return this.day; }
    setDay(value) { this.day = value; }

    getMonth() { return this.month; }
    setMonth(value) { this.month = value; }

    getYear() { return this.year; }
    setYear(value) { this.year = value; }

    getFirstName() { return this.firstName; }
    setFirstName(value) { this.firstName = value; }

    getLastName() { return this.lastName; }
    setLastName(value) { this.lastName = value; }

    getAddress() { return this.address; }
    setAddress(value) { this.address = value; }

    getCountry() { return this.country; }
    setCountry(value) { this.country = value; }

    getState() { return this.state; }
    setState(value) { this.state = value; }

    getCity() { return this.city; }
    setCity(value) { this.city = value; }

    getZipcode() { return this.zipcode; }
    setZipcode(value) { this.zipcode = value; }

    getMobileNumber() { return this.mobileNumber; }
    setMobileNumber(value) { this.mobileNumber = value; }
}

